# Lists in Python are a flexible and commonly used data type that can store collections of items. 
# They support ordered collections and allow elements of various types, such as integers, strings, or even nested lists. 
# Lists are defined by enclosing items in square brackets and separating them with commas, e.g., my_list = [1, 2, 3].
# Rules for writing and using Python lists:
# 1. Lists are created using square brackets, e.g., my_list = [1, 2, 3].
# 2. Elements in a list can be of any data type and can even include other lists (nested lists).
# 3. List indexing starts at 0, and negative indexing can be used to access elements from the end.
# 4. Lists are mutable, meaning you can modify their content, such as adding, removing, or updating elements.
# 5. Use list comprehensions to create lists in a concise, efficient way, e.g., squares = [x**2 for x in range(5)].


